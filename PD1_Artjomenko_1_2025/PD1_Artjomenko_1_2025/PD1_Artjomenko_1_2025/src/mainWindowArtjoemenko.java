    private void RegistrationButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_RegistrationButtonMouseClicked
        String name = jTextField1.getText();
        String lastname = jTextField2.getText();
        String nickname = jTextField3.getText();
        String password = new String(jPasswordField1.getPassword());
        String confirmPassword = new String(jPasswordField2.getPassword()); // otrais paroles lauks

        if (name.isEmpty() || lastname.isEmpty() || nickname.isEmpty() || password.isEmpty() || confirmPassword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Lūdzu, aizpildiet visus laukus, ieskaitot abas paroles!", "Kļūda", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Pārbaude: vai parole satur gan burtus, gan ciparus
        if (!password.matches("^(?=.*[a-zA-Z])(?=.*\\d).+$")) {
            JOptionPane.showMessageDialog(this, "Parolei jāietver gan burti, gan cipari!", "Nepietiekama parole", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Pārbaude: vai paroles sakrīt
        if (!password.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, "Paroles nesakrīt!", "Kļūda", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Saglabājam atbildes failā
        try (FileWriter writer = new FileWriter("dataArtjomenko.txt", true)) {
            writer.write("Vārds: " + name + "\n");
            writer.write("Uzvārds: " + lastname + "\n");
            writer.write("Lietotājvārds: " + nickname + "\n");
            writer.write("Parole: " + password + "\n");
            JOptionPane.showMessageDialog(this, "Reģistrēšana ir veiksmīgi pabeigta!");
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Kļūda saglabājot failu!", "Kļūda", JOptionPane.ERROR_MESSAGE);
        }

        // Atver logu tikai, ja viss kārtībā
        LoginWindow.setBounds(0, 0, 800, 600);
        LoginWindow.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
        LoginWindow.setLocationRelativeTo(null); // Centrē logu ekrānā
        LoginWindow.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_RegistrationButtonMouseClicked

