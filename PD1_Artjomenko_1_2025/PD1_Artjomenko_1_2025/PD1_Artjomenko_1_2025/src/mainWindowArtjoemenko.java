        answers[7] = a8;
        if (a8.equalsIgnoreCase("Rat")) correct++;

        // Jautājums 9
        String a9 = (String) jComboBox3.getSelectedItem();
        answers[8] = a9;
        if (a9.equalsIgnoreCase("Fish")) correct++;

        // Jautājums 10
        if (jRadioButton16.isSelected()) { correct++; answers[9] = "Hamster"; }
        else if (jRadioButton13.isSelected()) answers[9] = "Pig";
        else if (jRadioButton14.isSelected()) answers[9] = "Chicken";
        else if (jRadioButton15.isSelected()) answers[9] = "Duck";

        // Jautājums 11
        String a11 = jTextField8.getText().trim();
        answers[10] = a11;
        if (a11.equalsIgnoreCase("Pig")) correct++;

        // Jautājums 12
        String a12 = (String) jComboBox4.getSelectedItem();
        answers[11] = a12;
        if (a12.equalsIgnoreCase("Bear")) correct++;

        // Jautājums 13
        if (jRadioButton17.isSelected()) { correct++; answers[12] = "Chicken"; }
        else if (jRadioButton18.isSelected()) answers[12] = "Deer";
        else if (jRadioButton19.isSelected()) answers[12] = "Elk";
        else if (jRadioButton20.isSelected()) answers[12] = "Owl";

        // Jautājums 14
        String a14 = jTextField9.getText().trim();
        answers[13] = a14;
        if (a14.equalsIgnoreCase("Owl")) correct++;

        // Jautājums 15
        String a15 = (String) jComboBox5.getSelectedItem();
        answers[14] = a15;
        if (a15.equalsIgnoreCase("Bee")) correct++;

        // Saglabāt atbildes failā
        try (BufferedWriter bw = new BufferedWriter(new FileWriter("answersArtjomenko.txt"))) {
            for (int i = 0; i < answers.length; i++) {
                bw.write((i + 1) + ". jautājums: " + answers[i]);
                bw.newLine();
            }
        }

        // Procentu aprēķins un rezultāta izvadīšana
        int percentage = (int) Math.round(correct * 100.0 / 15);
        String grade;
        if (percentage >= 90) grade = "10";
        else if (percentage >= 80) grade = "9";
        else if (percentage >= 70) grade = "8";
        else if (percentage >= 60) grade = "7";
        else if (percentage >= 50) grade = "6";
        else if (percentage >= 40) grade = "5";
        else grade = "Nepietiekami";

        // Saglabāt rezultātu failā
