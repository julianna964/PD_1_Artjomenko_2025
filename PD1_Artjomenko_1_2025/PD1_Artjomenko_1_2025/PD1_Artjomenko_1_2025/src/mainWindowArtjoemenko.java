    private void LoginButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LoginButtonMouseClicked
    String enteredNickname = jTextField6.getText();
    String enteredPassword = new String(jPasswordField3.getPassword());

    boolean loginSuccess = false;

    try (BufferedReader reader = new BufferedReader(new FileReader("dataArtjomenko.txt"))) {
        String line;
        String nickname = null;
        String password = null;

        while ((line = reader.readLine()) != null) {
            if (line.startsWith("Lietotājvārds: ")) {
                nickname = line.substring("Lietotājvārds: ".length()).trim();
            } else if (line.startsWith("Parole: ")) {
                password = line.substring("Parole: ".length()).trim();

                // Pārbaudām, kad ir gan lietotājvārds, gan parole
                if (enteredNickname.equals(nickname) && enteredPassword.equals(password)) {
                    loginSuccess = true;
                    break;
                }
            }
        }

        if (loginSuccess) {
            JOptionPane.showMessageDialog(this, "Veiksmīga ielagošanās!", "Piekļuve atļauta", JOptionPane.INFORMATION_MESSAGE);

            // Atveram logu tikai tad, ja ielogošanās ir veiksmīga
            TestIntroducion.setBounds(0, 0, 800, 600);
            TestIntroducion.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
            TestIntroducion.setVisible(true);
            TestIntroducion.setLocationRelativeTo(null); // Centrē logu ekrānā
            LoginWindow.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Nepareizs lietotājvārds vai parole!", "Kļūda", JOptionPane.ERROR_MESSAGE);
        }

    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Kļūda lasot failu!", "Kļūda", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
        
        }
        
        
    }//GEN-LAST:event_LoginButtonMouseClicked

